    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
          String id,fName,lName,mNo,Adr,Age,query;
        String SUser, SUrl, Spass;
        SUrl = "jdbc:mysql://localhost/e-library";
        SUser = "root";
        Spass = "";
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(SUrl, SUser, Spass);
            Statement st = con.createStatement();
            if("".equals(mID.getText())){
                JOptionPane.showMessageDialog(new JFrame(),",Member ID is required" , "Error", JOptionPane.ERROR_MESSAGE);
            }else if("".equals(mF_Name.getText())){
                JOptionPane.showMessageDialog(new JFrame(),"First Name is required" , "Error", JOptionPane.ERROR_MESSAGE);
            }else if("".equals(mL_Name.getText())){
                JOptionPane.showMessageDialog(new JFrame(),"Last Name is required" , "Error", JOptionPane.ERROR_MESSAGE);
            }else if("".equals(mContact.getText())){
                JOptionPane.showMessageDialog(new JFrame(),"Author of the book is required" , "Error", JOptionPane.ERROR_MESSAGE);
                    }
            else if ("".equals(mAdr.getText())){
                JOptionPane.showMessageDialog(new JFrame(),"Price of the book is required" , "Error", JOptionPane.ERROR_MESSAGE);
                    }else if("".equals(mAge.getText())){
                JOptionPane.showMessageDialog(new JFrame(),"Name ofthe Distributor is required" , "Error", JOptionPane.ERROR_MESSAGE);
                    }
                    else{
                        id = mID.getText();
                        fName = mF_Name.getText();
                        lName  = mL_Name.getText();
                        mNo = mContact.getText();
                        Adr = mAdr.getText();
                        Age = mAge.getText();
                        System.out.println(id);
                        
                        query = "INSERT INTO members(m_ID, F_Name, L_Name, M_No, address, age)"+
                                "VALUES('"+id+"', '"+fName+"', '"+lName+"', '"+mNo+"','"+Adr+"','"+Age+"')";
                                
                        st.execute(query);       
                        mID.setText("");
                        mF_Name.setText("");
                        mL_Name.setText("");
                        mContact.setText("");
                        mAdr.setText("");
                        mAge.setText("");
                        
                    
                        showMessageDialog(null, "The Member Has Been Added Successfully!");
                    }
            
        }catch(Exception e){
            System.out.println("Error!"+e.getMessage());
        }
         
    }//GEN-LAST:event_jButton1ActionPerformed

