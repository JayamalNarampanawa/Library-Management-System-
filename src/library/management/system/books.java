    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id,Name,ISBN,Author,Price,Distributors,query;
        String SUser, SUrl, Spass;
        SUrl = "jdbc:mysql://localhost/e-library";
        SUser = "root";
        Spass = "";
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(SUrl, SUser, Spass);
            Statement st = con.createStatement();
            if("".equals(bookID.getText())){
                JOptionPane.showMessageDialog(new JFrame(),"Book ID is required" , "Error", JOptionPane.ERROR_MESSAGE);
            }else if("".equals(bName.getText())){
                JOptionPane.showMessageDialog(new JFrame(),"Book Name is required" , "Error", JOptionPane.ERROR_MESSAGE);
            }else if("".equals(ISBNno.getText())){
                JOptionPane.showMessageDialog(new JFrame(),"ISBN number is required" , "Error", JOptionPane.ERROR_MESSAGE);
            }else if("".equals(bAuthor.getText())){
                JOptionPane.showMessageDialog(new JFrame(),"Author of the book is required" , "Error", JOptionPane.ERROR_MESSAGE);
                    }
            else if ("".equals(bPrice.getText())){
                JOptionPane.showMessageDialog(new JFrame(),"Price of the book is required" , "Error", JOptionPane.ERROR_MESSAGE);
                    }else if("".equals(bDist.getText())){
                JOptionPane.showMessageDialog(new JFrame(),"Name ofthe Distributor is required" , "Error", JOptionPane.ERROR_MESSAGE);
                    }
                    else{
                        id = bookID.getText();
                        Name = bName.getText();
                        ISBN  = ISBNno.getText();
                        Author = bAuthor.getText();
                        Price = bPrice.getText();
                        Distributors = bDist.getText();
                        System.out.println(Distributors);
                        
                        query = "INSERT INTO books(B_ID, B_Name, ISBN, Auth, Price, distributors)"+
                                "VALUES('"+id+"', '"+Name+"', '"+ISBN+"', '"+Author+"','"+Price+"','"+Distributors+"')";
                                
                        st.execute(query);       
                        bookID.setText("");
                        bName.setText("");
                        ISBNno.setText("");
                        bAuthor.setText("");
                        bPrice.setText("");
                        bDist.setText("");
                        
                    
                        showMessageDialog(null, "The Book Has Been Added Successfully!");
 


                        
                        
                        
                        
                        
                        
                    }
            }catch(Exception e){
            System.out.println("Error!"+e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

